/**
 * Validate all credentials for a deployment
 */
export async function validateAllCredentials(creds: Partial<AgentCredentials>): Promise<CredentialValidation[]> {
  const results: CredentialValidation[] = []

  if (creds.whatsapp_number) {
    results.push(validateWhatsAppNumber(creds.whatsapp_number))
  }

  if (creds.website_url) {
    results.push(await validateWebsiteUrl(creds.website_url))
  }

  if (creds.openai_api_key) {
    results.push(validateOpenAIKey(creds.openai_api_key))
  }

  if (creds.groq_api_key) {
    results.push(validateGroqKey(creds.groq_api_key))
  }

  return results
}

